COMMENT ON COLUMN PRODUCT.ITINERARY IS '여행일정';
COMMENT ON COLUMN PRODUCT.ITINERARY_IMAGE IS '여행일정이미지파일명';
COMMENT ON COLUMN PRODUCT.BANNER_IMAGE IS '여행배너이미지파일명';
COMMENT ON COLUMN PRODUCT.PRODUCT_PRICE IS '상품가격';
COMMENT ON COLUMN PRODUCT.PASSENGER_COUNT IS '승객수';
COMMENT ON COLUMN PRODUCT.D_DEPARTURE_DATE_TIME IS '출국출발일/시(yyyy-mm-dd hh24:mi)';
COMMENT ON COLUMN PRODUCT.D_ARRIVAL_DATE_TIME IS '출국도착일/시(yyyy-mm-dd hh24:mi)';
COMMENT ON COLUMN PRODUCT.E_DEPARTURE_DATE_TIME IS '입국출발일/시(yyyy-mm-dd hh24:mi)';
